    private void botonSeleccionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonSeleccionarActionPerformed
        try{
            int fila=jTable1.getSelectedRow();
            
            String descripcion=tabla1.getValueAt(fila, 2).toString();
            double saldo=Double.parseDouble(tabla1.getValueAt(fila, 1).toString());
            String mes=tabla1.getValueAt(fila, 3).toString();
            int anio=Integer.parseInt(tabla1.getValueAt(fila, 0).toString());
            
            DepositoMensual dp= new DepositoMensual(anio, Mes.getMesXName(mes), saldo, descripcion);
            MenuPrincipal m=new MenuPrincipal(dp);
            m.setVisible(true);
            m.setLocationRelativeTo(null);
            this.dispose();
        }catch (Exception e){
            JOptionPane.showMessageDialog(null, "Error= "+e.getMessage() + " porque: "+e.getCause());
        }
        
    }//GEN-LAST:event_botonSeleccionarActionPerformed


    private void CargarCabeceras(){
        tabla1.addColumn("año");
        tabla1.addColumn("depositado");
        tabla1.addColumn("descripcion");
        tabla1.addColumn("mes");
        jTable1.setModel(tabla1);
    }
    private void cargarTabla(){
        saldos.stream().forEach(s->tabla1.addRow(new Object[]{s.getAnio(),s.getDepositado(),s.getDescripcion(),s.getMes()}));
    }

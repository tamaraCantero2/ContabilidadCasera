     public void calcularSaldoHastaLaFecha(){
        try{
        Mes mesSelected=Mes.getMesXName(mes.getSelectedItem().toString());
        ArrayList<DepositoMensual> depositosHastaMesSelected=controllerDepositos.buscarDepositosXMes(mesSelected.obtenerStringMes());
        List<Compra> comprasMesSelected=controllerCompras.buscarXMes(mesSelected);
        double saldoDepositos=0.0,saldoCompras=0.0;
        if(depositosHastaMesSelected.size()>1){
            int o=JOptionPane.showConfirmDialog(null, "Se encontraron mas de un deposito en el mismo mes, desea sumarlos?");
            if(o==0){
                 saldoDepositos=depositosHastaMesSelected.stream().mapToDouble(c->c.getDepositado()).sum();
                 saldoCompras=comprasMesSelected.stream().mapToDouble(c->c.getPrecio()).sum();
        
                double saldoRestante=saldoDepositos-saldoCompras;
        
            }else{
               seleccionarSaldo s1= new seleccionarSaldo(depositosHastaMesSelected);
               s1.setVisible(true);
               this.dispose();
               
               
            }
        } 
        double saldoRestante=saldoDepositos-saldoCompras;
        
        saldoTotalMesDepositos.setText("SALDO TOTAL DEL MES HASTA LA FECHA: "+saldoDepositos);
        saldoRestanteMesDepositos.setText("SALDO RESTANTE DEL MES HASTA LA FECHA: "+saldoRestante);
        }catch(Exception e){
        
        }
    }
